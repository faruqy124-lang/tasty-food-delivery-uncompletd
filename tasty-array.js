function updateFoodSlider() {
  const foodSlider = document.getElementById("food-slider");
  const translateValue = -(currentFoodIndex * 25); // âœ… shift by 25% = 1 item
  foodSlider.style.transform = `translateX(${translateValue}%)`;
}

function nextFoodSlide() {
  const foods = foodData[currentFoodType];
  if (currentFoodIndex < foods.length - itemsPerPage) {
    currentFoodIndex++;
    updateFoodSlider();
  }
}

function prevFoodSlide() {
  if (currentFoodIndex > 0) {
    currentFoodIndex--;
    updateFoodSlider();
  }
}

